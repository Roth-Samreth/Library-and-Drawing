import tkinter as tk
root = tk.Tk()
root.geometry("800x700")
frame = tk.Frame()
frame.master.title("Oval")
canvas = tk.Canvas(frame)
canvas.create_oval(100, 100, 300, 200, fill='#fff')
canvas.create_oval(170, 110, 230, 190, fill='blue')
canvas.create_oval(100, 100, 300, 200, fill='#fff') 
canvas.create_oval(170, 110, 230, 190, fill='#ff00ff')
canvas.create_oval(500, 100, 700, 200, fill='#fff')
canvas.create_oval(570, 110, 630, 190, fill='blue')
canvas.create_oval(350, 250, 450, 350, fill='#ff0000')
canvas.create_rectangle(100, 450, 150, 580, fill='red')
canvas.create_rectangle(650, 480, 700, 580, fill='red')
canvas.create_rectangle(100, 580, 700, 530, fill='red')
canvas.pack(expand=True, fill='both')
frame.pack(expand=True, fill='both')
root.mainloop()
